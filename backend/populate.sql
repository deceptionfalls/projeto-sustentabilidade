DROP TABLE IF EXISTS tbEcoponto;
CREATE TABLE tbEcoponto (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    nome_local VARCHAR NOT NULL,
    endereco VARCHAR NOT NULL,
    imagem_url VARCHAR,
    cidade VARCHAR NOT NULL,
    estado VARCHAR(2) NOT NULL,
    telefone VARCHAR(9),
    latitude FLOAT NOT NULL,
    longitude FLOAT NOT NULL,
    hora_aberto TIME,
    hora_fechado TIME,
    aceita_entulho BOOLEAN NOT NULL DEFAULT 0,
    aceita_construcao BOOLEAN NOT NULL DEFAULT 0,
    aceita_papel BOOLEAN NOT NULL DEFAULT 0,
    aceita_papelao BOOLEAN NOT NULL DEFAULT 0,
    aceita_vidro BOOLEAN NOT NULL DEFAULT 0,
    aceita_metal BOOLEAN NOT NULL DEFAULT 0,
    aceita_movel BOOLEAN NOT NULL DEFAULT 0,
    aceita_poda_arvore BOOLEAN NOT NULL DEFAULT 0,
    aceita_bateria BOOLEAN NOT NULL DEFAULT 0,
    aceita_monitor BOOLEAN NOT NULL DEFAULT 0,
    aceita_celular BOOLEAN NOT NULL DEFAULT 0,
    aceita_impressora BOOLEAN NOT NULL DEFAULT 0
);
